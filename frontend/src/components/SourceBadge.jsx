export function SourceBadge({ source }) {
  if (!source) return <span style={{ color: "var(--text-muted)", fontSize: 12 }}>—</span>;
  const cfg = SOURCE_CONFIG[source];
  const color = cfg?.color || "#6B7280";
  const Icon = cfg?.icon;
  const label = SOURCE_LABELS[source] || source.replace(/_/g, " ");
  return (
    <span style={{
      display: "inline-flex", alignItems: "center", gap: 5,
      fontSize: 11, fontWeight: 700, padding: "2px 10px",
      borderRadius: 99, background: `${color}12`, color,
      border: `1px solid ${color}28`, width: "fit-content",
      whiteSpace: "nowrap",
    }}>
      {Icon && <Icon size={14} />}
      {label}
    </span>
  );
}
